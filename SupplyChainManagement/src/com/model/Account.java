package com.model;

public class Account {

	private int id;
	private int balance;
	
	public Account() {
		id = 0;
		balance = 0;
	}
	
	public void setID(int id) {
		this.id = id;
	}
	
	public int getID() {
		return this.id;
	}
	
	public void setBalance(int id) {
		this.id = id;
	}
	
	public int getBalance(int id) {
		return this.balance;
	}
}
